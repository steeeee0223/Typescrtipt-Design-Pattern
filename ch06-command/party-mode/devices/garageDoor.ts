export class GarageDoor {
    public up() {
        console.log("Garage door is up...");
    }

    public down() {
        console.log("Garage door is down...");
    }

    public stop() {
        console.log("Garage door stopped ...");
    }

    public lightOn() {
        console.log("Light of garage door is on...");
    }

    public lightOff() {
        console.log("Light of garage door is off...");
    }
}
